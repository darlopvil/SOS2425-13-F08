    <h2>Descripción del proyecto</h2>
    <p style="font-size: 20px;">
        Our project is based on the integration of data on forest fires, natural parks, 
            and water supply aid, aiming to analyze the relationship between the availability of this resource
            and its impact on environmental and social management.
         Through this data convergence, we seek to identify patterns that enhance fire prevention and response, 
            optimize ecosystem conservation in protected areas, and assess the effectiveness of subsidies 
            aimed at mitigating water scarcity in vulnerable municipalities.
         This comprehensive approach will contribute to the development of more efficient and sustainable strategies 
            for managing water as an essential resource.
    </p>
